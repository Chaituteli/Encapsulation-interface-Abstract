package com.vstl.oops.encapsulation;

public class Instagram {
	
	private String strURL="";
	private String strUserName="";
	private String strPassword="";
	private String strDateofBirth="";
	private String strPlace="";
	private String strEmailId="";
	
	public String getStrURL() {
		return strURL;
	}
	public void setStrURL(String strURL) {
		this.strURL = strURL;
	}
	public String getStrUserName() {
		return strUserName;
	}
	public void setStrUserName(String strUserName) {
		this.strUserName = strUserName;
	}
	public String getStrPassword() {
		return strPassword;
	}
	public void setStrPassword(String strPassword) {
		this.strPassword = strPassword;
	}
	public String getStrDateofBirth() {
		return strDateofBirth;
	}
	public void setStrDateofBirth(String strDateofBirth) {
		this.strDateofBirth = strDateofBirth;
	}
	public String getStrPlace() {
		return strPlace;
	}
	public void setStrPlace(String strPlace) {
		this.strPlace = strPlace;
	}
	public String getStrEmailId() {
		return strEmailId;
	}
	public void setStrEmailId(String strEmailId) {
		this.strEmailId = strEmailId;
	}
	
	

}
